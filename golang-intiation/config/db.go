package config

import (
	"log"
	"os"

	"github.com/joho/godotenv"
	"gorm.io/driver/mysql"
	"gorm.io/gorm"
)

func GetEnVariables(key string) string {
	err := godotenv.Load(".env")
	if err != nil {
		log.Fatal("Error loading .env file")
	}

	return os.Getenv(key)
}

func ConnectToDatabase() *gorm.DB {

	dbUsername := GetEnVariables("DB_USERNAME")
	dbName := GetEnVariables("DB_NAME")

	dsn := dbUsername + ":" + "@tcp(127.0.0.1:3306)/" + dbName + "?charset=utf8mb4&parseTime=True&loc=Local"
	db, err := gorm.Open(mysql.Open(dsn), &gorm.Config{})

	if err != nil {
		print(err.Error())
	}

	return db
}
